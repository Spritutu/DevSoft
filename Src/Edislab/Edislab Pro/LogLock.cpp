CLogLock::CLogLock( void )
{
	MUTEX_INIT(m_Lock);
}

CLogLock::~CLogLock( void )
{
	MUTEX_DEL(m_Lock);
}

void CLogLock::Lock( void )
{
	LOCK(m_Lock);
}

void CLogLock::UnLock( void )
{
	UNLOCK(m_Lock);
}


